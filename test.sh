#!/bin/bash
# ОИБ Практика 4 — Разграничение прав доступа
# Выполнять пошагово в терминале, не запускать весь скрипт сразу!

# 1. Получить права суперпользователя
sudo su

# 2. Изучить справку по useradd (просто почитать)
man useradd
# Нас интересует флаг -m (создать домашний каталог) и -d (путь к нему)

# 3. Создать пользователя sit2 с домашним каталогом /home/sit2
useradd -m -d /home/sit2 sit2

# 4. Установить пароль для sit2
passwd sit2

# 5. Выйти из root
exit

# 6. Войти под sit (терминал 1) и sit2 (терминал 2)
# Терминал 1:
su sit
# Терминал 2:
su sit2

# 7. Посмотреть идентификаторы пользователей
id sit
id sit2
# Покажет uid, gid и группы каждого пользователя

# 8. Посмотреть права на домашние каталоги
ls -la /home/sit
ls -la /home/sit2
# drwxr-xr-x — владелец может всё, остальные только читать и заходить

# 9. Под sit2: создать файл с маской 0077
# (0077 — группа и остальные не имеют никаких прав)
umask 0077
echo "hello" > ~/testfile
ls -l ~/testfile
# Права будут: -rw------- (только владелец читает и пишет)

# 10. Под sit: попробовать прочитать файл sit2 (должен отказать)
cat /home/sit2/testfile
# Вывод: Permission denied

# 11. Под root: изменить права — sit может писать, но не читать
sudo chmod o+w,o-r /home/sit2/testfile
ls -l /home/sit2/testfile
# Права будут: -rw----w- (others: только запись)

# 12. Под sit: записать текст в файл через nano
nano /home/sit2/testfile
# Написать любой текст, Ctrl+O сохранить, Ctrl+X выйти

# 13. Под sit2: проверить права и прочитать содержимое
ls -l ~/testfile
cat ~/testfile
# sit2 видит текст, записанный пользователем sit

# 14. Под sit2: создать каталог
mkdir ~/mydir

# 15. Установить права записи для группы на каталог
chmod g+w ~/mydir
ls -la ~/ | grep mydir
# Права: drwxrwx--- (группа может писать)

# 16. Под root: добавить sit в группу sit2
sudo usermod -aG sit2 sit

# 17. Проверить группы пользователя sit
groups sit
# Вывод покажет sit2 в списке групп

# 18. Под sit: создать несколько файлов в каталоге sit2
# (нужно перелогиниться чтобы группа применилась)
su sit
touch /home/sit2/mydir/file1
touch /home/sit2/mydir/file2
ls /home/sit2/mydir/

# 19. Изучить справку по удалению пользователя
man userdel
# Флаг -r удаляет домашний каталог и почту вместе с пользователем

# 20. Под root: удалить sit2 вместе с домашним каталогом
sudo userdel -r sit2
# Проверить что каталог удалён:
ls /home/
